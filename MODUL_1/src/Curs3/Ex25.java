package Curs3;

public class Ex25 {
    public static void main(String[] args) {
        /*
        5. Observa precizia valorilor numerice memorate de urmatoarele tipuri de date: int, float si double. Pentru aceasta, scrie si tu in Eclipse urmatoarea portiune de cod:
         */
        int intValue = 10 / 3;
        float floatValue = 10f / 3f;
        double doubleValue = 10d / 3d;

        System.out.println("Integer value is: " + intValue);
        System.out.println("Float value is: " + floatValue);
        System.out.println("Double value is: " + doubleValue);
    }
}
